package beans;

/**
 *
 * @author Abel
 */
public class Rectangulo {
    private int base = 5;
    private int altura = 10;

    public int getBase() {
        return base;
    }

    public int getAltura() {
        return altura;
    }

    public void setBase(int base) {
        this.base = base;
    }

    public void setAltura(int altura) {
        this.altura = altura;
    }
    
    public int getArea(){
        return this.base * this.altura;
    }
}
